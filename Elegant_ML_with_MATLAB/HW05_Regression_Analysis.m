%% 1. 金属块温度的三次多项式拟合
% 已知time和temp数据，要求：
% ① 画出time-temp散点图(scatter)
% ② 使用三次多项式拟合、在同一坐标上用plot画出曲线（带legend）
% ③ 求9小时的温度
clear
clc
close all % 关闭之前的图形窗口
disp('--- 第1题：运行中 ---')

time = [0, 0.5, 1.0, 1.5, 2.0, 2.5, 3.0, 3.5, 4.0, 5.0, 6.0, 6.1, 7.0];
temp = [300, 281, 261, 244, 228, 214, 202, 191, 181, 164, 151, 149, 141];

figure('Name', '温度变化拟合');
% ① 画出散点图
scatter(time, temp, 50, 'r', 'filled'); 
hold on; % 必须加 hold on，否则多项式曲线会覆盖散点图

% ② 三次多项式拟合 (保留你使用的 regress 方法)
% 注意：Time矩阵的每一列分别是 1, t, t^2, t^3
Time_mat = [ones(length(time),1), time', (time.^2)', (time.^3)'];
[b, bint, r, rint, stats] = regress(temp', Time_mat);

% 计算拟合曲线的y值并绘制
y_fit = b(1) + b(2)*time + b(3)*time.^2 + b(4)*time.^3;
plot(time, y_fit, 'b-', 'LineWidth', 1.5);

xlabel('时间 time (h)');
ylabel('温度 temp (F)');
title('金属块温度随时间变化的三次多项式拟合');
legend('实际温度散点', '三次多项式拟合曲线');

% ③ 求9小时的温度
y_9 = b(1) + b(2)*9 + b(3)*9^2 + b(4)*9^3;
fprintf('第1题结果：9小时的温度预计为 %.2f F\n\n', y_9);


%% 2. 体育成绩多元线性回归
% 根据15名同学的跳远、1000米、1分钟跳绳成绩，线性合成综合打分
clear
clc
disp('--- 第2题：运行中 ---')


x1 = [180, 201, 205, 208, 213, 217, 218, 222, 226, 230, 233, 238, 240, 242, 253];
x2 = [280, 240, 226, 224, 220, 217, 225, 221, 211, 213, 199, 198, 195, 186, 183];
x3 = [153, 170, 162, 160, 162, 165, 170, 168, 169, 179, 172, 172, 175, 181, 176];
Y  = [60,  75,  70,  70,  75,  75,  85,  80,  80,  85,  90,  90,  90,  95,  95]';

% 构建设计矩阵 X 并进行多元线性回归
X = [ones(15,1), x1', x2', x3'];
[b, bint, r, rint, stats] = regress(Y, X);

% 计算拟合出的综合打分
y_fit = b(1) + b(2).*x1 + b(3).*x2 + b(4).*x3;

% 绘图展示
figure('Name', '体育成绩多元线性回归');
plot(x1, 'b.-', 'LineWidth', 1.5); 
hold on;
plot(x2, 'g.-', 'LineWidth', 1.5);
plot(x3, 'k.-', 'LineWidth', 1.5);
plot(Y, 'mo', 'MarkerSize', 8, 'LineWidth', 1.5); % 实际综合打分 (品红色圆圈)
plot(y_fit, 'r*-', 'LineWidth', 1.5);             % 拟合综合打分 (红色星号实线)

axis([0 16 0 300]);
xlabel('样本编号 (1-15)');
ylabel('各项成绩数值');
title('三项指标与综合打分的线性回归模型对比');
legend('跳远成绩(cm)', '1000米成绩(s)', '1分钟跳绳(个)', '实际综合打分', '模型拟合综合打分', 'Location', 'best');

% 输出回归系数和方程以便在命令行查看
fprintf('第2题结果：多元线性回归公式为:\n');
fprintf('综合分数 = %.4f + (%.4f * 跳远) + (%.4f * 1000米) + (%.4f * 跳绳)\n', b(1), b(2), b(3), b(4));